package basic_java.day1;

class PrimitiveDataType {
    static byte b;
    static short s;
    static int i;
    static long l;
    static float f;
    static double d;
    static boolean bool;
    static char c;
    public static void main(String[] args) {
        System.out.println("Default values of all data types in java: ");
        System.out.println("byte type value = " + b);
        System.out.println("short type value = " + s);
        System.out.println("int type value = " + i);
        System.out.println("long type value = " + l);
        System.out.println("float type value = " + f);
        System.out.println("double type value = " + d);
        System.out.println("boolean type value = " + bool);
        System.out.println("char type value = " + c);
    }
}
